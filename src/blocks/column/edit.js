import styling from "./styling"
import columnSettings from "./settings";
import React, { useEffect } from 'react';

const { withSelect } = wp.data

const columnComponent = props => {

	useEffect(() => { // Replacement for componentDidMount.

		// Assigning block_id in the attribute.
		props.setAttributes( { block_id: props.clientId.substr( 0, 8 ) } )

		props.setAttributes( { classMigrate: true } )

		// Pushing Style tag for this block css.
		const $style = document.createElement( "style" )
		$style.setAttribute( "id", "uagb-column-style-" + props.clientId.substr( 0, 8 ) )
		document.head.appendChild( $style )
	}, [])

	useEffect(() => { // Replacement for componentDidUpdate.
		var element = document.getElementById( "uagb-column-style-" + props.clientId.substr( 0, 8 ) )

		if( null !== element && undefined !== element ) {
			element.innerHTML = styling( props )
		}
	}, [props] )

	return (
		<>
			{ columnSettings( props ) }
			{ renderColumn( props ) }

		</>
	)
}

export default withSelect( ( select, props ) => {
	const { __experimentalGetPreviewDeviceType = null } = select( "core/edit-post" )
	let deviceType = __experimentalGetPreviewDeviceType ? __experimentalGetPreviewDeviceType() : null

	return {
		deviceType: deviceType,
	}
} )( columnComponent )